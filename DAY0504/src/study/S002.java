package study;
import java.util.*;
public class S002 {
	public static void main(String[] args){
		/* -50에서 1까지의 수를 출력하는 프로그램
		 * 단, 한줄에 5개씩만 출력하고 이 수들 사이를 탭간격으로 띄우세요!
		 * while문을 사용하세요
		 */
		/*
		 * int a; int c=1; a=-50; while(a<=1) { System.out.printf("%d%c",a,((c++%5!=0)?'
		 * ':'\n')); a++;}
		 */
		// 잘못한건 시스템 아웃을 while문 밖에다가 사용한거..!
		/*if(i%5==0)System.out.printf("%d\t",i)
		 * i++;
		 * if(i%5==0)
		 * System.out.println();
		 */
		/*while문을 이용해서 콜라 사이다 마운틴 초코송이 에이스 웨하스가 나오는
		 * 자판기프로그램을 만드세요
		 * 단 0번을 입력하면 종료하세요
		 */
		Scanner sc= new Scanner(System.in);
		int u=-1;
		
		
		while(u !=0) {
			System.out.print("1.콜라2.사이다3.마운틴4.초코송이5.에이스6.웨하스0.종료");
			u=sc.nextInt();
			switch (u) {
			case 1:
				System.out.println("콜라");
				break;
			case 2:
				System.out.println("사이다");
				break;
			case 3:
				System.out.println("마운틴");
				break;
			case 4:
				System.out.println("초코송이");
				break;
			case 5:
				System.out.println("에이스");
				break;
			case 6:
				System.out.println("웨하스");
				break;
			default:
				System.out.println("찾으시는 상품이 없습니다.");
				break;
			}
		
		}
		
	}

}
